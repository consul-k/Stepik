orders = (
    (101, (("Пицца", 2), ("Салат", 1), ("Кола", 3))),
    (102, (("Пицца", 1), ("Сок", 2))),
    (103, (("Салат", 2), ("Кофе", 1))),
    (104, (("Пицца", 3), ("Сок", 1), ("Кофе", 2))),
    (105, (("Кола", 2), ("Салат", 1), ("Пицца", 2)))
)

# далее ваш код
n = input()
res = 0
for order in orders:
    for i in order[1]:
        if n in i:
            res += i[1]
print(f'Общее количество заказов "{n}": {res}')
