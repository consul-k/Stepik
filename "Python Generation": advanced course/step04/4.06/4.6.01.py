n = [int(s) for s in input().split()]
matrix = []

for i in range(n[0]):
    p = []
    if i%2 == 0:
        for j in range(n[1]):
            if j%2 == 0:
                p.append('.')
            if j%2 != 0:
                p.append('*')
    if i%2 != 0:
        for j in range(n[1]):
            if j%2 == 0:
                p.append('*')
            if j%2 != 0:
                p.append('.')
    matrix.append(p)
            
for i in matrix:
    print(*i,sep=' ')
