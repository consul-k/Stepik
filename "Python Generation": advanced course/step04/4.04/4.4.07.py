    for j in range(len(matrix)):
        if i > j and i < len(matrix) - 1 - j:
            left.append(matrix[i][j])
        if i < j and i > len(matrix) - 1 - j:
            right.append(matrix[i][j])
        if i < j and i < len(matrix) - 1 - j:
            up.append(matrix[i][j])
        if i > j and i > len(matrix) - 1 - j:
            down.append(matrix[i][j])

