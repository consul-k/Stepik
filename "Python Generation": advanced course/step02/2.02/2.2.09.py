'''

Дана строка текста, состоящая из букв русского алфавита "О" и "Р". 
Буква "О" соответствует выпадению Орла, а буква "Р" - выпадению Решки. 
Напишите программу, которая подсчитывает наибольшее количество подряд выпавших Решек.

Формат входных данных
На вход программе подается строка текста, состоящая из букв русского алфавита "О" и "Р".

Формат выходных данных
Программа должна вывести наибольшее количество подряд выпавших Решек.

Примечание. Если выпавших Решек нет, то необходимо вывести число 00.
Тестовые данные 🟢

Sample Input 1:

ОРРОРОРООРРРО

Sample Output 1:

3

Sample Input 2:

ООООООРРРОРОРРРРРРР

Sample Output 2:

7

Sample Input 3:

ООООРРРРОРОРРРРРРРРООРОРОРРРРРРРРРРРРРРРРРРРРРРРРРРРРРРР

Sample Output 3:

31

'''

a = input()
cnt = 0
cnt_max = 0
for s in range(len(a)):
    if a[s] == 'Р':
        cnt+=1
        if cnt > cnt_max:
            cnt_max = cnt
    else:
        cnt = 0
print(cnt_max)
