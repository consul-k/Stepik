s = input()

print(len(s))

# Исходная строка, повторенная 3 раза
print(s * 3)

# Первый символ строки
print(s[0])

# Первые три символа строки
print(s[:3])

# Последние три символа строки
print(s[-3:])

# Строка в обратном порядке
print(s[::-1])

# Строка с удаленным первым и последним символом
print(s[1:-1])
