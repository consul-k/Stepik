def пройти_через_астероиды(количество, шаг=1):
    if количество <= 0:
        print(f"Шаг {шаг}: Поле пройдено!")
        return
    
    if количество == 1:
        print(f"Шаг {шаг}: Остался 1 астероид")
    elif количество % 10 == 1 and количество % 100 != 11:
        print(f"Шаг {шаг}: Остался {количество} астероид")
    elif количество % 10 in [2, 3, 4] and количество % 100 not in [12, 13, 14]:
        print(f"Шаг {шаг}: Осталось {количество} астероида")
    else:
        print(f"Шаг {шаг}: Осталось {количество} астероидов")
    
    пройти_через_астероиды(количество - 1, шаг + 1)
