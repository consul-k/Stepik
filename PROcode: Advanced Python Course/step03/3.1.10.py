# –í–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö
health = int(input())                       # –∑–¥–æ—Ä–æ–≤—å–µ –ö—Ä–∞–∫–µ–Ω–∞
damage_list = list(map(int, input().split()))  # —Å–ø–∏—Å–æ–∫ —É—Ä–æ–Ω–∞ –ø–∏—Ä–∞—Ç–æ–≤

# –ü–µ—Ä–µ–±–æ—Ä –∞—Ç–∞–∫ –ø–∏—Ä–∞—Ç–æ–≤
for i, damage in enumerate(damage_list, start=1):
    # –í—ã—á–∏—Å–ª—è–µ–º –Ω–æ–≤–æ–µ –∑–¥–æ—Ä–æ–≤—å–µ –ö—Ä–∞–∫–µ–Ω–∞, –Ω–µ –¥–∞—ë–º –µ–º—É —É–π—Ç–∏ –Ω–∏–∂–µ 0
    health = max(0, health - damage)
    
    # –í—ã–≤–æ–¥ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ–± –∞—Ç–∞–∫–µ
    print(f"–ü–∏—Ä–∞—Ç {i} –∞—Ç–∞–∫—É–µ—Ç! –£ –ö—Ä–∞–∫–µ–Ω–∞ –æ—Å—Ç–∞–ª–æ—Å—å {health} –∂–∏–∑–Ω–µ–π.")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –ø–æ–≤–µ—Ä–∂–µ–Ω –ª–∏ –ö—Ä–∞–∫–µ–Ω
    if health == 0:
        print("–ß—ë—Ä–Ω—ã–π –ö—Ä–∞–∫–µ–Ω –ø–æ–≤–µ—Ä–∂–µ–Ω! –ö–ª–∞–¥ —Ç–≤–æ–π! üèÜüí∞")
        print(f"–ü–æ—Å–ª–µ–¥–Ω–∏–π —É–¥–∞—Ä –Ω–∞–Ω—ë—Å –ø–∏—Ä–∞—Ç ‚Ññ{i}!")
        break
else:
    # –ï—Å–ª–∏ —Ü–∏–∫–ª –∑–∞–∫–æ–Ω—á–∏–ª—Å—è –±–µ–∑ –ø–æ–±–µ–¥—ã
    print(f"–ö—Ä–∞–∫–µ–Ω –Ω–µ –ø–æ–±–µ–∂–¥—ë–Ω, —Ç–∞–∫ –∫–∞–∫ –æ—Å—Ç–∞–ª–æ—Å—å {health} –∂–∏–∑–Ω–µ–π.")
